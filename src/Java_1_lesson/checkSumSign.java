package Java_1_lesson;

public class checkSumSign {
    public static void main(String[] args) {

        int a = 7;
        int b = 5;
        int sum = a + b;

        if (sum >= 0) {
            System.out.println("Сумма положительная");
        } else
            System.out.println("Сумма отрицательная");
    }
}
